package FirstPackage;

public class class1 {
	public static void main(String[] args) {

		for(int a=1;a<=5;a++) {
			
			for(int b=1;b<=a ;b++) {
				System.out.print("*");
			}
			
			System.out.println();
		}
		System.out.println("______________________");
		System.out.println();
		for(int i=1;i<=5;i++) {
			for(int j=i;j<=5;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}